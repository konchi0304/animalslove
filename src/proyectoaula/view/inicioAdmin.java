    public void cargarTrabajadoresEnTabla() {
        trabajadoresDAO trabajadorDAO = new trabajadoresDAO();
        List<trabajadores> listaTrabajadores = trabajadorDAO.obtenerTrabajadores();

        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0); 

        for (trabajadores trabajador : listaTrabajadores) {
            model.addRow(new Object[]{
                trabajador.getIdentificacion(),
                trabajador.getNombre(),
                trabajador.getApellidos(),
                trabajador.getEdad(),
                trabajador.getCargo(),
                trabajador.getTelefono(),
                trabajador.getUser(),
                trabajador.getContraseña(),
               
            });
        }
     }
    
