       public void iniciarOpciones() {
        JMenuItem open = new JMenuItem("Mas Info");
        JMenuItem editar = new JMenuItem("Editar");
        JMenuItem eliminar = new JMenuItem("Despedir");
        
        ppMenuTabla.add(open);
        ppMenuTabla.add(editar);
        ppMenuTabla.add(eliminar);

        table.setComponentPopupMenu(ppMenuTabla);

       
        eliminar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                int selectedRow = table.getSelectedRow();

                if (selectedRow != -1) { 
                    
                    String identificacion = (String) table.getValueAt(selectedRow, 0);

                    
                    int confirm = JOptionPane.showConfirmDialog(null, "seguro de que deseas eliminar al trabajador?", "Confirmación", JOptionPane.YES_NO_OPTION);
                    if (confirm == JOptionPane.YES_OPTION) {
                       
                        trabajadoresDAO trabajadoresDAO = new trabajadoresDAO();
                        trabajadoresDAO.eliminarTrabajador(identificacion);

                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Seleccione un trabajador para eliminar.");
                }
            }
        });
    }

